/**
 * Get genre configuration for a game type
 */
export function getGenreConfig(gameType: string): GenreConfig {
  return GENRE_REGISTRY[gameType] || GENRE_REGISTRY['rpg'];
}

/**
 * Get all available game genres
 */
export function getAllGenres(): GenreConfig[] {
  return Object.values(GENRE_REGISTRY);
}

/**
 * Check if a game type is valid
 */
export function isValidGameType(gameType: string): boolean {
  return gameType in GENRE_REGISTRY;
}

