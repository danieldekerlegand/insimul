function generateAssetLoader(): string {
  return `/**
 * Asset Loader
 * Centralized asset loading with progress tracking and caching.
 * Generated by Insimul
 */

import { Scene } from '@babylonjs/core/scene';
import { SceneLoader } from '@babylonjs/core/Loading/sceneLoader';
import { Texture } from '@babylonjs/core/Materials/Textures/texture';
import { Sound } from '@babylonjs/core/Audio/sound';
import type { ISceneLoaderAsyncResult } from '@babylonjs/core/Loading/sceneLoader';

export interface AssetManifest {
  version: string;
  generatedAt: string;
  totalFiles: number;
  totalSizeBytes: number;
  categories: Record<string, { files: ManifestEntry[] }>;
}

interface ManifestEntry {
  exportPath: string;
  originalName: string;
  sizeBytes: number;
  mimeType: string;
}

export type LoadProgress = {
  loaded: number;
  total: number;
  percent: number;
  currentFile: string;
};

type ProgressCallback = (progress: LoadProgress) => void;

export class AssetLoader {
  private scene: Scene;
  private modelCache = new Map<string, ISceneLoaderAsyncResult>();
  private textureCache = new Map<string, Texture>();
  private soundCache = new Map<string, Sound>();
  private manifest: AssetManifest | null = null;

  constructor(scene: Scene) {
    this.scene = scene;
  }

  async loadManifest(url = '/asset-manifest.json'): Promise<AssetManifest> {
    if (this.manifest) return this.manifest;

    try {
      const resp = await fetch(url);
      this.manifest = await resp.json();
      console.log('[AssetLoader] Manifest loaded:', this.manifest!.totalFiles, 'files');
    } catch {
      console.warn('[AssetLoader] No manifest found, assets will load on demand');
      this.manifest = { version: '0', generatedAt: '', totalFiles: 0, totalSizeBytes: 0, categories: {} };
    }
    return this.manifest!;
  }

  async loadModel(dir: string, file: string): Promise<ISceneLoaderAsyncResult> {
    const key = dir + file;
    const cached = this.modelCache.get(key);
    if (cached) return cached;

    const result = await SceneLoader.ImportMeshAsync('', dir, file, this.scene);
    this.modelCache.set(key, result);
    return result;
  }

  loadTexture(url: string): Texture {
    const cached = this.textureCache.get(url);
    if (cached) return cached;

    const tex = new Texture(url, this.scene);
    this.textureCache.set(url, tex);
    return tex;
  }

  loadSound(name: string, url: string, options: { loop?: boolean; autoplay?: boolean; volume?: number } = {}): Sound {
    const cached = this.soundCache.get(name);
    if (cached) return cached;

    const sound = new Sound(name, url, this.scene, null, {
      loop: options.loop ?? false,
      autoplay: options.autoplay ?? false,
      volume: options.volume ?? 1.0,
    });
    this.soundCache.set(name, sound);
    return sound;
  }

  async preloadAll(onProgress?: ProgressCallback): Promise<void> {
    const manifest = await this.loadManifest();
    const allFiles: { category: string; entry: ManifestEntry }[] = [];

    for (const [cat, data] of Object.entries(manifest.categories)) {
      for (const entry of data.files) {
        allFiles.push({ category: cat, entry });
      }
    }

    const total = allFiles.length;
    let loaded = 0;

    for (const { category, entry } of allFiles) {
      const path = '/assets/' + entry.exportPath;

      try {
        if (entry.mimeType.includes('gltf') || entry.mimeType.includes('glb') || entry.mimeType.includes('octet-stream')) {
          const dir = path.substring(0, path.lastIndexOf('/') + 1);
          const file = path.substring(path.lastIndexOf('/') + 1);
          await this.loadModel(dir, file);
        } else if (entry.mimeType.startsWith('image/')) {
          this.loadTexture(path);
        } else if (entry.mimeType.startsWith('audio/')) {
          this.loadSound(entry.originalName, path);
        }
      } catch (err) {
        console.warn('[AssetLoader] Failed to preload', path, err);
      }

      loaded++;
      onProgress?.({
        loaded,
        total,
        percent: Math.round((loaded / total) * 100),
        currentFile: entry.originalName,
      });
    }

    console.log('[AssetLoader] Preload complete:', loaded, '/', total, 'assets');
  }

  getModel(dir: string, file: string): ISceneLoaderAsyncResult | undefined {
    return this.modelCache.get(dir + file);
  }

  getTexture(url: string): Texture | undefined {
    return this.textureCache.get(url);
  }

  getSound(name: string): Sound | undefined {
    return this.soundCache.get(name);
  }

  dispose(): void {
    for (const result of this.modelCache.values()) {
      for (const mesh of result.meshes) mesh.dispose();
    }
    for (const tex of this.textureCache.values()) tex.dispose();
    for (const snd of this.soundCache.values()) snd.dispose();
    this.modelCache.clear();
    this.textureCache.clear();
    this.soundCache.clear();
  }
}
`;
}

