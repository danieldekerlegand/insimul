function generateNPCSpawner(): string {
  return `/**
 * NPC Spawner
 * Creates NPC meshes and controllers from data.
 * Loads GLB models from bundled assets with procedural fallback.
 */

import { Scene } from '@babylonjs/core/scene';
import { Vector3 } from '@babylonjs/core/Maths/math';
import { Color3 } from '@babylonjs/core/Maths/math.color';
import { MeshBuilder } from '@babylonjs/core/Meshes/meshBuilder';
import { StandardMaterial } from '@babylonjs/core/Materials/standardMaterial';
import { SceneLoader } from '@babylonjs/core/Loading/sceneLoader';
import type { AbstractMesh } from '@babylonjs/core/Meshes/abstractMesh';
import type { ShadowGenerator } from '@babylonjs/core/Lights/Shadows/shadowGenerator';
import { NPCController, type NPCData } from './npc-controller';
import type { DialogueSystem } from './systems/dialogue-system';

/** Maps NPC roles to GLB model filenames in /assets/characters/ */
const ROLE_MODEL_MAP: Record<string, string> = {
  guard: 'npc_guard.glb',
  merchant: 'npc_merchant.glb',
  questgiver: 'npc_merchant.glb',
  civilian: 'npc_civilian_male.glb',
};

const ROLE_COLORS: Record<string, [number, number, number]> = {
  questgiver: [1, 0.85, 0],
  merchant: [0.2, 0.8, 0.2],
  guard: [0.6, 0.6, 0.7],
  civilian: [0.8, 0.6, 0.4],
};

export class NPCSpawner {
  private npcs: NPCController[] = [];
  private npcDataList: NPCData[];
  private dialogueSystem: DialogueSystem;

  constructor(npcDataList: NPCData[], dialogueSystem: DialogueSystem) {
    this.npcDataList = npcDataList;
    this.dialogueSystem = dialogueSystem;
  }

  async spawn(scene: Scene, shadowGen: ShadowGenerator): Promise<void> {
    for (const data of this.npcDataList) {
      let mesh: AbstractMesh;

      // Try loading a role-specific GLB model
      const modelFile = ROLE_MODEL_MAP[data.role] || 'npc_civilian_male.glb';
      try {
        const result = await SceneLoader.ImportMeshAsync(
          '',
          '/assets/characters/',
          modelFile,
          scene,
        );
        if (result.meshes.length > 0) {
          mesh = result.meshes[0];
          mesh.name = \`npc_\${data.characterId}\`;
          mesh.position = new Vector3(
            data.homePosition.x,
            data.homePosition.y,
            data.homePosition.z,
          );
          mesh.scaling = new Vector3(1, 1, 1);
          mesh.checkCollisions = true;
          shadowGen.addShadowCaster(mesh);
        } else {
          throw new Error('No meshes in GLB');
        }
      } catch {
        // Fallback: procedural capsule
        const capsule = MeshBuilder.CreateCapsule(
          \`npc_\${data.characterId}\`,
          { height: 1.8, radius: 0.35 },
          scene,
        );
        capsule.position = new Vector3(
          data.homePosition.x,
          data.homePosition.y + 0.9,
          data.homePosition.z,
        );
        capsule.checkCollisions = true;

        const mat = new StandardMaterial(\`npcMat_\${data.characterId}\`, scene);
        const color = ROLE_COLORS[data.role] || ROLE_COLORS.civilian;
        mat.diffuseColor = new Color3(...color);
        capsule.material = mat;

        shadowGen.addShadowCaster(capsule);
        mesh = capsule;
      }

      const controller = new NPCController(mesh as any, data);
      this.npcs.push(controller);
    }

    console.log(\`[NPCSpawner] Spawned \${this.npcs.length} NPCs\`);
  }

  update(dt: number, playerPos: Vector3): void {
    for (const npc of this.npcs) {
      npc.update(dt, playerPos);
    }
  }

  /** Find nearest interactable NPC within range */
  findNearest(playerPos: Vector3, maxRange = 3): NPCController | null {
    let closest: NPCController | null = null;
    let closestDist = maxRange;

    for (const npc of this.npcs) {
      const dist = Vector3.Distance(playerPos, npc.mesh.position);
      if (dist < closestDist) {
        closestDist = dist;
        closest = npc;
      }
    }

    return closest;
  }
}
`;
}

