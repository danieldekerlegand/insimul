function bakeVec3(v: { x: number; y: number; z: number } | [number, number, number]): string {
  if (Array.isArray(v)) return `new Vector3(${v[0]}, ${v[1]}, ${v[2]})`;
  return `new Vector3(${v.x}, ${v.y}, ${v.z})`;
}

