function generatePlayer(ir: WorldIR): string {
  return `/**
 * Player Controller
 * 3rd-person character with WASD movement, jump, and interaction.
 * Loads GLB model from bundled assets with procedural fallback.
 */

import { Scene } from '@babylonjs/core/scene';
import { Vector3 } from '@babylonjs/core/Maths/math';
import { MeshBuilder } from '@babylonjs/core/Meshes/meshBuilder';
import { StandardMaterial } from '@babylonjs/core/Materials/standardMaterial';
import { Color3 } from '@babylonjs/core/Maths/math.color';
import { ArcRotateCamera } from '@babylonjs/core/Cameras/arcRotateCamera';
import { SceneLoader } from '@babylonjs/core/Loading/sceneLoader';
import { AbstractMesh } from '@babylonjs/core/Meshes/abstractMesh';
import type { Mesh } from '@babylonjs/core/Meshes/mesh';

export interface PlayerConfig {
  speed: number;
  jumpForce: number;
  health: number;
  maxHealth: number;
  energy: number;
  maxEnergy: number;
}

export class Player {
  mesh: Mesh | AbstractMesh;
  scene: Scene;
  camera: ArcRotateCamera;
  config: PlayerConfig;

  // State
  health: number;
  maxHealth: number;
  energy: number;
  maxEnergy: number;
  isGrounded = true;
  velocity = Vector3.Zero();

  // Input
  private keys: Record<string, boolean> = {};

  get position(): Vector3 {
    return this.mesh.position;
  }

  constructor(scene: Scene, camera: ArcRotateCamera, mesh: Mesh | AbstractMesh, config: PlayerConfig) {
    this.scene = scene;
    this.camera = camera;
    this.mesh = mesh;
    this.config = config;
    this.health = config.health;
    this.maxHealth = config.maxHealth;
    this.energy = config.energy;
    this.maxEnergy = config.maxEnergy;

    // Input listeners
    window.addEventListener('keydown', (e) => { this.keys[e.key.toLowerCase()] = true; });
    window.addEventListener('keyup', (e) => { this.keys[e.key.toLowerCase()] = false; });
  }

  update(dt: number): void {
    const speed = this.config.speed * dt;
    const forward = this.camera.getForwardRay().direction;
    forward.y = 0;
    forward.normalize();
    const right = Vector3.Cross(forward, Vector3.Up()).normalize();

    const move = Vector3.Zero();
    if (this.keys['w'] || this.keys['arrowup']) move.addInPlace(forward.scale(speed));
    if (this.keys['s'] || this.keys['arrowdown']) move.addInPlace(forward.scale(-speed));
    if (this.keys['a'] || this.keys['arrowleft']) move.addInPlace(right.scale(-speed));
    if (this.keys['d'] || this.keys['arrowright']) move.addInPlace(right.scale(speed));

    // Jump
    if ((this.keys[' '] || this.keys['space']) && this.isGrounded) {
      this.velocity.y = this.config.jumpForce;
      this.isGrounded = false;
    }

    // Gravity
    this.velocity.y -= 9.81 * dt;

    // Apply movement
    this.mesh.position.addInPlace(move);
    this.mesh.position.addInPlace(this.velocity.scale(dt));

    // Ground collision (simple)
    if (this.mesh.position.y <= 1) {
      this.mesh.position.y = 1;
      this.velocity.y = 0;
      this.isGrounded = true;
    }

    // Face movement direction
    if (move.length() > 0.001) {
      const angle = Math.atan2(move.x, move.z);
      this.mesh.rotation.y = angle;
    }

    // Camera follow
    this.camera.target = this.mesh.position.clone();

    // Energy regen
    this.energy = Math.min(this.maxEnergy, this.energy + 2 * dt);
  }

  takeDamage(amount: number): void {
    this.health = Math.max(0, this.health - amount);
  }

  heal(amount: number): void {
    this.health = Math.min(this.maxHealth, this.health + amount);
  }
}

export async function createPlayer(scene: Scene, camera: ArcRotateCamera, playerData: any): Promise<Player> {
  let mesh: Mesh | AbstractMesh;

  // Try to load GLB model from bundled assets
  try {
    const result = await SceneLoader.ImportMeshAsync(
      '',
      '/assets/characters/',
      'player_default.glb',
      scene,
    );
    if (result.meshes.length > 0) {
      mesh = result.meshes[0];
      mesh.name = 'player';
      mesh.position = new Vector3(0, 0, 0);
      mesh.scaling = new Vector3(1, 1, 1);
      mesh.checkCollisions = true;
      console.log('[Player] Loaded GLB model');
    } else {
      throw new Error('No meshes in GLB');
    }
  } catch (err) {
    console.warn('[Player] GLB not available, using procedural mesh:', err);
    // Fallback: procedural capsule
    const capsule = MeshBuilder.CreateCapsule('player', { height: 2, radius: 0.4 }, scene);
    capsule.position = new Vector3(0, 1, 0);
    capsule.checkCollisions = true;
    const mat = new StandardMaterial('playerMat', scene);
    mat.diffuseColor = new Color3(0.2, 0.6, 1.0);
    capsule.material = mat;
    mesh = capsule;
  }

  const config: PlayerConfig = {
    speed: playerData?.moveSpeed || 8,
    jumpForce: playerData?.jumpForce || 5,
    health: playerData?.startingHealth || 100,
    maxHealth: playerData?.startingHealth || 100,
    energy: playerData?.startingEnergy || 100,
    maxEnergy: playerData?.startingEnergy || 100,
  };

  return new Player(scene, camera, mesh, config);
}
`;
}

