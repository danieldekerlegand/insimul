function generateWorldGenerator(ir: WorldIR): string {
  return `/**
 * World Generator
 * Procedurally generates terrain, buildings, roads, and nature objects.
 */

import { Scene } from '@babylonjs/core/scene';
import { Vector3, Color3 } from '@babylonjs/core/Maths/math';
import { MeshBuilder } from '@babylonjs/core/Meshes/meshBuilder';
import { StandardMaterial } from '@babylonjs/core/Materials/standardMaterial';
import { Texture } from '@babylonjs/core/Materials/Textures/texture';
import { SceneLoader } from '@babylonjs/core/Loading/sceneLoader';
import type { ShadowGenerator } from '@babylonjs/core/Lights/Shadows/shadowGenerator';

// Map building types/names to KayKit GLTF models
const BUILDING_MODEL_MAP: Record<string, string> = {
  house: 'building_home_A_blue.gltf',
  home: 'building_home_A_blue.gltf',
  residence: 'building_home_A_blue.gltf',
  tavern: 'building_tavern_blue.gltf',
  inn: 'building_tavern_blue.gltf',
  blacksmith: 'building_blacksmith_blue.gltf',
  forge: 'building_blacksmith_blue.gltf',
  smith: 'building_blacksmith_blue.gltf',
  church: 'building_church_blue.gltf',
  temple: 'building_church_blue.gltf',
  chapel: 'building_church_blue.gltf',
  barracks: 'building_barracks_blue.gltf',
  garrison: 'building_barracks_blue.gltf',
  guardhouse: 'building_barracks_blue.gltf',
  castle: 'building_castle_blue.gltf',
  keep: 'building_castle_blue.gltf',
  fortress: 'building_castle_blue.gltf',
  market: 'building_market_blue.gltf',
  shop: 'building_market_blue.gltf',
  store: 'building_market_blue.gltf',
  mill: 'building_windmill_blue.gltf',
  windmill: 'building_windmill_blue.gltf',
  lumbermill: 'building_lumbermill_blue.gltf',
  mine: 'building_mine_blue.gltf',
  well: 'building_well_blue.gltf',
};

export class WorldGenerator {
  private geography: any;
  private buildings: any[];
  private roads: any[];
  private nature: any[];

  constructor(geography: any, buildings: any[], roads: any[], nature: any[]) {
    this.geography = geography;
    this.buildings = buildings;
    this.roads = roads;
    this.nature = nature;
  }

  async generate(scene: Scene, shadowGen: ShadowGenerator): Promise<void> {
    this.generateTerrain(scene);
    await this.generateBuildings(scene, shadowGen);
    this.generateRoads(scene);
    this.generateNature(scene, shadowGen);
  }

  private generateTerrain(scene: Scene): void {
    const size = this.geography.terrainSize || ${ir.geography.terrainSize};
    const ground = MeshBuilder.CreateGroundFromHeightMap(
      'ground',
      '/assets/ground/ground_heightMap.png',
      {
        width: size,
        height: size,
        subdivisions: 128,
        minHeight: 0,
        maxHeight: 5,
      },
      scene,
    );
    ground.receiveShadows = true;
    ground.checkCollisions = true;

    const mat = new StandardMaterial('groundMat', scene);

    // Load bundled ground textures
    try {
      const diffuse = new Texture('/assets/ground/ground.jpg', scene);
      diffuse.uScale = size / 10;
      diffuse.vScale = size / 10;
      mat.diffuseTexture = diffuse;

      const normal = new Texture('/assets/ground/ground-normal.png', scene);
      normal.uScale = size / 10;
      normal.vScale = size / 10;
      mat.bumpTexture = normal;
    } catch {
      // Fallback to flat color if textures not available
      mat.diffuseColor = new Color3(0.35, 0.55, 0.25);
    }

    mat.specularColor = new Color3(0.05, 0.05, 0.05);
    ground.material = mat;
  }

  private resolveBuildingModel(bldg: any): string | null {
    // Try matching by building type, name, or business type
    const typeName = (bldg.buildingType || bldg.type || bldg.name || '').toLowerCase();
    for (const [key, model] of Object.entries(BUILDING_MODEL_MAP)) {
      if (typeName.includes(key)) return model;
    }
    return null;
  }

  private async generateBuildings(scene: Scene, shadowGen: ShadowGenerator): Promise<void> {
    let glbLoaded = 0;
    let proceduralFallback = 0;

    for (const bldg of this.buildings) {
      const pos = bldg.position || { x: 0, y: 0, z: 0 };
      const rotation = bldg.rotation || 0;
      const modelFile = this.resolveBuildingModel(bldg);

      if (modelFile) {
        try {
          const result = await SceneLoader.ImportMeshAsync(
            '',
            '/assets/buildings/',
            modelFile,
            scene,
          );
          if (result.meshes.length > 0) {
            const root = result.meshes[0];
            root.position = new Vector3(pos.x, 0, pos.z);
            root.rotation.y = rotation;
            root.checkCollisions = true;

            for (const mesh of result.meshes) {
              mesh.checkCollisions = true;
              shadowGen.addShadowCaster(mesh);
            }
            glbLoaded++;
            continue;
          }
        } catch {
          // Fall through to procedural
        }
      }

      // Procedural fallback
      const spec = bldg.spec || {};
      const w = spec.width || 6;
      const h = spec.floors ? spec.floors * 3 : spec.height || 5;
      const d = spec.depth || 6;

      const box = MeshBuilder.CreateBox(\`bldg_\${bldg.id}\`, { width: w, height: h, depth: d }, scene);
      box.position = new Vector3(pos.x, h / 2, pos.z);
      box.rotation.y = rotation;
      box.checkCollisions = true;

      const mat = new StandardMaterial(\`bldgMat_\${bldg.id}\`, scene);
      const style = bldg.style || {};
      const wallColor = style.wallColor;
      if (wallColor) {
        mat.diffuseColor = new Color3(wallColor.r || 0.7, wallColor.g || 0.65, wallColor.b || 0.6);
      } else {
        mat.diffuseColor = new Color3(0.7, 0.65, 0.6);
      }
      box.material = mat;

      shadowGen.addShadowCaster(box);

      // Simple pitched roof
      if (spec.floors || spec.roofType) {
        const roof = MeshBuilder.CreateCylinder(\`roof_\${bldg.id}\`, {
          diameterTop: 0,
          diameterBottom: Math.max(w, d) * 1.3,
          height: 2.5,
          tessellation: 4,
        }, scene);
        roof.position = new Vector3(pos.x, h + 1.25, pos.z);
        roof.rotation.y = rotation;

        const roofMat = new StandardMaterial(\`roofMat_\${bldg.id}\`, scene);
        const roofColor = style.roofColor;
        if (roofColor) {
          roofMat.diffuseColor = new Color3(roofColor.r || 0.5, roofColor.g || 0.3, roofColor.b || 0.2);
        } else {
          roofMat.diffuseColor = new Color3(0.5, 0.3, 0.2);
        }
        roof.material = roofMat;
        shadowGen.addShadowCaster(roof);
      }
      proceduralFallback++;
    }

    console.log(\`[World] Generated \${this.buildings.length} buildings (\${glbLoaded} GLB, \${proceduralFallback} procedural)\`);
  }

  private generateRoads(scene: Scene): void {
    const roadMat = new StandardMaterial('roadMat', scene);
    roadMat.diffuseColor = new Color3(0.35, 0.35, 0.35);
    roadMat.specularColor = new Color3(0.05, 0.05, 0.05);

    for (const road of this.roads) {
      if (!road.path || road.path.length < 2) continue;

      // Simple flat boxes along path segments
      for (let i = 0; i < road.path.length - 1; i++) {
        const a = road.path[i];
        const b = road.path[i + 1];
        const dx = b.x - a.x;
        const dz = b.z - a.z;
        const len = Math.sqrt(dx * dx + dz * dz);
        const angle = Math.atan2(dx, dz);
        const width = road.width || 4;

        const seg = MeshBuilder.CreateBox(\`road_\${road.id}_\${i}\`, {
          width: width,
          height: 0.05,
          depth: len,
        }, scene);
        seg.position = new Vector3(
          (a.x + b.x) / 2,
          0.025,
          (a.z + b.z) / 2,
        );
        seg.rotation.y = angle;
        seg.material = roadMat;
      }
    }

    console.log(\`[World] Generated \${this.roads.length} roads\`);
  }

  private generateNature(scene: Scene, shadowGen: ShadowGenerator): void {
    for (const obj of this.nature) {
      const pos = obj.position || { x: 0, y: 0, z: 0 };
      const kind = (obj.objectType || obj.kind || 'tree').toLowerCase();

      if (kind.includes('tree')) {
        // Trunk
        const trunk = MeshBuilder.CreateCylinder(\`trunk_\${obj.id}\`, {
          height: 3,
          diameterTop: 0.3,
          diameterBottom: 0.5,
        }, scene);
        trunk.position = new Vector3(pos.x, 1.5, pos.z);
        const trunkMat = new StandardMaterial(\`trunkMat_\${obj.id}\`, scene);
        trunkMat.diffuseColor = new Color3(0.4, 0.25, 0.1);
        trunk.material = trunkMat;

        // Canopy
        const canopy = MeshBuilder.CreateSphere(\`canopy_\${obj.id}\`, {
          diameter: 3 + Math.random() * 2,
          segments: 8,
        }, scene);
        canopy.position = new Vector3(pos.x, 4, pos.z);
        const canopyMat = new StandardMaterial(\`canopyMat_\${obj.id}\`, scene);
        canopyMat.diffuseColor = new Color3(0.15 + Math.random() * 0.1, 0.5 + Math.random() * 0.2, 0.1);
        canopy.material = canopyMat;

        shadowGen.addShadowCaster(trunk);
        shadowGen.addShadowCaster(canopy);
      } else if (kind.includes('rock') || kind.includes('boulder')) {
        const rock = MeshBuilder.CreateSphere(\`rock_\${obj.id}\`, {
          diameter: 1 + Math.random() * 1.5,
          segments: 6,
        }, scene);
        rock.position = new Vector3(pos.x, 0.5, pos.z);
        rock.scaling = new Vector3(1, 0.6 + Math.random() * 0.4, 1);
        const rockMat = new StandardMaterial(\`rockMat_\${obj.id}\`, scene);
        rockMat.diffuseColor = new Color3(0.5, 0.5, 0.5);
        rock.material = rockMat;
        shadowGen.addShadowCaster(rock);
      } else if (kind.includes('bush') || kind.includes('shrub')) {
        const bush = MeshBuilder.CreateSphere(\`bush_\${obj.id}\`, {
          diameter: 1.2 + Math.random(),
          segments: 6,
        }, scene);
        bush.position = new Vector3(pos.x, 0.5, pos.z);
        const bushMat = new StandardMaterial(\`bushMat_\${obj.id}\`, scene);
        bushMat.diffuseColor = new Color3(0.2, 0.45, 0.15);
        bush.material = bushMat;
        shadowGen.addShadowCaster(bush);
      }
    }

    console.log(\`[World] Generated \${this.nature.length} nature objects\`);
  }
}
`;
}

