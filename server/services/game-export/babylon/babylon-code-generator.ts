function generateNPCController(): string {
  return `/**
 * NPC Controller
 * Handles individual NPC behaviour: idle, patrol, interact.
 */

import { Vector3 } from '@babylonjs/core/Maths/math';
import type { Mesh } from '@babylonjs/core/Meshes/mesh';

export type NPCState = 'idle' | 'patrol' | 'interact' | 'combat';

export interface NPCData {
  characterId: string;
  name: string;
  role: string;
  homePosition: { x: number; y: number; z: number };
  patrolRadius: number;
  disposition: number;
  greeting: string | null;
  questIds: string[];
}

export class NPCController {
  mesh: Mesh;
  data: NPCData;
  state: NPCState = 'idle';
  private patrolTarget: Vector3 | null = null;
  private stateTimer = 0;
  private idleDuration = 3 + Math.random() * 4;

  constructor(mesh: Mesh, data: NPCData) {
    this.mesh = mesh;
    this.data = data;
  }

  update(dt: number, playerPos: Vector3): void {
    this.stateTimer += dt;

    switch (this.state) {
      case 'idle':
        if (this.stateTimer > this.idleDuration) {
          this.state = 'patrol';
          this.stateTimer = 0;
          this.pickPatrolTarget();
        }
        break;

      case 'patrol':
        if (this.patrolTarget) {
          const dir = this.patrolTarget.subtract(this.mesh.position);
          dir.y = 0;
          if (dir.length() < 0.5) {
            this.state = 'idle';
            this.stateTimer = 0;
            this.idleDuration = 3 + Math.random() * 4;
          } else {
            dir.normalize();
            this.mesh.position.addInPlace(dir.scale(2 * dt));
            this.mesh.rotation.y = Math.atan2(dir.x, dir.z);
          }
        }
        break;

      case 'interact':
        // Face the player
        const toPlayer = playerPos.subtract(this.mesh.position);
        toPlayer.y = 0;
        if (toPlayer.length() > 0.1) {
          this.mesh.rotation.y = Math.atan2(toPlayer.x, toPlayer.z);
        }
        break;
    }
  }

  private pickPatrolTarget(): void {
    const home = this.data.homePosition;
    const r = this.data.patrolRadius;
    const angle = Math.random() * Math.PI * 2;
    const dist = Math.random() * r;
    this.patrolTarget = new Vector3(
      home.x + Math.cos(angle) * dist,
      home.y,
      home.z + Math.sin(angle) * dist,
    );
  }

  startInteraction(): void {
    this.state = 'interact';
    this.stateTimer = 0;
  }

  endInteraction(): void {
    this.state = 'idle';
    this.stateTimer = 0;
  }
}
`;
}

