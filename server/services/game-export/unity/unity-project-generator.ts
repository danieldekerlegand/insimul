/**
 * Unity Project Generator
 *
 * Generates the Unity project scaffolding: Packages/manifest.json,
 * ProjectSettings, assembly definition, and README.
 */

import type { WorldIR } from '@shared/game-engine/ir-types';

export interface GeneratedFile {
  path: string;
  content: string;
}

// ─────────────────────────────────────────────
// Helpers
// ─────────────────────────────────────────────

function sanitiseName(name: string): string {
  return name.replace(/[^a-zA-Z0-9]/g, '');
}

// ─────────────────────────────────────────────
// Packages/manifest.json
// ─────────────────────────────────────────────

function generateManifest(): string {
  return JSON.stringify({
    dependencies: {
      'com.unity.inputsystem': '1.8.2',
      'com.unity.cinemachine': '2.10.1',
      'com.unity.ai.navigation': '2.0.4',
      'com.unity.textmeshpro': '3.2.0-pre.10',
      'com.unity.ugui': '2.0.0',
      'com.unity.modules.ai': '1.0.0',
      'com.unity.modules.animation': '1.0.0',
      'com.unity.modules.audio': '1.0.0',
      'com.unity.modules.imageconversion': '1.0.0',
      'com.unity.modules.jsonserialize': '1.0.0',
      'com.unity.modules.physics': '1.0.0',
      'com.unity.modules.ui': '1.0.0',
      'com.unity.modules.unitywebrequest': '1.0.0',
    },
  }, null, 2);
}

// ─────────────────────────────────────────────
// ProjectSettings
// ─────────────────────────────────────────────

function generateProjectSettingsYaml(ir: WorldIR): string {
  const worldSafe = sanitiseName(ir.meta.worldName);
  return `%YAML 1.1
%TAG !u! tag:unity3d.com,2011:
--- !u!129 &1
PlayerSettings:
  productName: InsimulExport_${worldSafe}
  companyName: Insimul
  defaultScreenWidth: 1920
  defaultScreenHeight: 1080
  runInBackground: 1
  activeInputHandler: 2
  apiCompatibilityLevel: 6
`;
}

function generateInputManagerYaml(): string {
  return `%YAML 1.1
%TAG !u! tag:unity3d.com,2011:
--- !u!13 &1
InputManager:
  m_ObjectHideFlags: 0
  serializedVersion: 2
  m_Axes: []
`;
}

function generateTagManagerYaml(): string {
  return `%YAML 1.1
%TAG !u! tag:unity3d.com,2011:
--- !u!78 &1
TagManager:
  serializedVersion: 2
  tags:
  - NPC
  - Building
  - Settlement
  - QuestObject
  - Resource
  layers:
  - Default
  - TransparentFX
  - Ignore Raycast
  - Water
  - UI
  - Terrain
  - Buildings
  - NPCs
  - Interactable
`;
}

function generateQualitySettingsYaml(): string {
  return `%YAML 1.1
%TAG !u! tag:unity3d.com,2011:
--- !u!47 &1
QualitySettings:
  m_ObjectHideFlags: 0
  serializedVersion: 5
  m_CurrentQuality: 3
`;
}

// ─────────────────────────────────────────────
// Assembly Definition
// ─────────────────────────────────────────────

function generateAsmDef(): string {
  return JSON.stringify({
    name: 'Insimul',
    rootNamespace: 'Insimul',
    references: [
      'Unity.InputSystem',
      'Unity.TextMeshPro',
      'Unity.AI.Navigation',
    ],
    includePlatforms: [],
    excludePlatforms: [],
    allowUnsafeCode: false,
    overrideReferences: false,
    precompiledReferences: [],
    autoReferenced: true,
    defineConstraints: [],
    versionDefines: [],
    noEngineReferences: false,
  }, null, 2);
}

