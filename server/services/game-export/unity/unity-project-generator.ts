// ─────────────────────────────────────────────
// README
// ─────────────────────────────────────────────

function generateReadme(ir: WorldIR): string {
  const g = ir.meta.genreConfig;
  return `# ${ir.meta.worldName} — Unity Export

Generated by Insimul v${ir.meta.insimulVersion} on ${ir.meta.exportTimestamp}

## Quick Start

1. **Requirements**: Unity 2022.3 LTS or newer (with New Input System package)
2. Open the project folder in Unity Hub → "Add project from disk"
3. Wait for package resolution and script compilation
4. Open \`Assets/Scenes/MainWorld.unity\` (or let InsimulDataLoader auto-create)
5. Press Play to test

## World Info

| Property | Value |
|---|---|
| World Name | ${ir.meta.worldName} |
| World Type | ${ir.meta.worldType || 'default'} |
| Genre | ${g.name} (${g.id}) |
| Terrain Size | ${ir.geography.terrainSize}×${ir.geography.terrainSize} |
| Countries | ${ir.geography.countries.length} |
| States | ${ir.geography.states.length} |
| Settlements | ${ir.geography.settlements.length} |
| Characters | ${ir.entities.characters.length} |
| NPCs | ${ir.entities.npcs.length} |
| Buildings | ${ir.entities.buildings.length} |
| Roads | ${ir.entities.roads.length} |
| Rules | ${ir.systems.rules.length} |
| Actions | ${ir.systems.actions.length} |
| Quests | ${ir.systems.quests.length} |

## Project Structure

\`\`\`
Assets/
├── Scripts/
│   ├── Core/            Game manager, data loader
│   ├── Characters/      Player + NPC controllers
│   ├── Systems/         Action, combat, quest, inventory, crafting, etc.
│   ├── World/           Procedural generators
│   └── UI/              HUD, minimap, inventory, dialogue, quest tracker
├── Resources/
│   └── Data/            JSON data files (WorldIR, characters, actions, etc.)
├── Prefabs/             Create prefabs for Player, NPC, Buildings, etc.
├── Scenes/              Main world scene
└── Materials/           Create materials in the editor
\`\`\`

## Data Loading

The game loads \`Resources/Data/WorldIR.json\` at runtime via
\`InsimulDataLoader.LoadWorldData()\`. Individual JSON files in
\`Resources/Data/\` can also be loaded independently.

## Notes

- Prefabs and Scenes must be created in the Unity Editor
- C# scripts provide all system logic; extend via inheritance or composition
- NPC AI uses Unity NavMesh — bake navigation after placing terrain
- UI uses Unity's Canvas system with TextMeshPro
- Input uses the New Input System (InputActions asset should be created in editor)
`;
}

// ═════════════════════════════════════════════
// Public API
// ═════════════════════════════════════════════

export function generateProjectFiles(ir: WorldIR): GeneratedFile[] {
  const files: GeneratedFile[] = [];

  // Package manifest
  files.push({ path: 'Packages/manifest.json', content: generateManifest() });

  // Project settings
  files.push({ path: 'ProjectSettings/ProjectSettings.asset', content: generateProjectSettingsYaml(ir) });
  files.push({ path: 'ProjectSettings/InputManager.asset', content: generateInputManagerYaml() });
  files.push({ path: 'ProjectSettings/TagManager.asset', content: generateTagManagerYaml() });
  files.push({ path: 'ProjectSettings/QualitySettings.asset', content: generateQualitySettingsYaml() });

  // Assembly definition
  files.push({ path: 'Assets/Scripts/Insimul.asmdef', content: generateAsmDef() });

  // Misc
  files.push({ path: '.gitignore', content: generateGitignore() });
  files.push({ path: 'README.md', content: generateReadme(ir) });

  return files;
}
