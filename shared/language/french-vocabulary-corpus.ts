/**
 * Search French corpus by English or French word
 */
export function searchFrenchCorpus(query: string): FrenchVocabularyEntry[] {
  const q = query.toLowerCase();
  const entries: FrenchVocabularyEntry[] = [];
  for (const cat of Object.keys(FRENCH_VOCABULARY_CORPUS) as FrenchVocabularyCategory[]) {
    entries.push(...FRENCH_VOCABULARY_CORPUS[cat].filter(e =>
      e.english.toLowerCase().includes(q) || e.french.toLowerCase().includes(q)
    ));
  }
  return entries;
}

