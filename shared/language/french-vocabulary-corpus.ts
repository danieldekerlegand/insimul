/**
 * Get the total number of entries in the French corpus
 */
export function getFrenchCorpusSize(): number {
  let total = 0;
  for (const cat of Object.keys(FRENCH_VOCABULARY_CORPUS) as FrenchVocabularyCategory[]) {
    total += FRENCH_VOCABULARY_CORPUS[cat].length;
  }
  return total;
}

/**
 * Get French entries by difficulty tier
 */
export function getFrenchCorpusByDifficulty(difficulty: 'beginner' | 'intermediate' | 'advanced'): FrenchVocabularyEntry[] {
  const entries: FrenchVocabularyEntry[] = [];
  for (const cat of Object.keys(FRENCH_VOCABULARY_CORPUS) as FrenchVocabularyCategory[]) {
    entries.push(...FRENCH_VOCABULARY_CORPUS[cat].filter(e => e.difficulty === difficulty));
  }
  return entries;
}

